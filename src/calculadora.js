let somarDoisNumeros = function somarDoisNumeros(numA, numB) {
    // retorna a soma dos 2 numeros
    return numA + numB
}

let subtrairDoisNumeros = (numA, numB) => numA - numB

let multiplicarDoisNumeros = (numA, numB) => numA * numB

let dividirDoisNumeros = (numA, numB) => numA / numB


module.exports = {
    somarDoisNumeros: somarDoisNumeros,
    subtrairDoisNumeros: subtrairDoisNumeros,
    multiplicarDoisNumeros: multiplicarDoisNumeros,
    dividirDoisNumeros: dividirDoisNumeros
}